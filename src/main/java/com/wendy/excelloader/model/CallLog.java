package com.wendy.excelloader.model;

public class CallLog {

    private String totalCallMessage;
    private int totalCallCount;

    public String getTotalCallMessage() {
        return totalCallMessage;
    }

    public void setTotalCallMessage(String totalCallMessage) {
        this.totalCallMessage = totalCallMessage;
    }

    public int getTotalCallCount() {
        return totalCallCount;
    }

    public void setTotalCallCount(int totalCallCount) {
        this.totalCallCount = totalCallCount;
    }
}
